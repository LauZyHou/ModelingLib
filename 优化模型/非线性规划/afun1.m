function [f] = afun1(x)
%AFUN1 用于ademo_fmincon1即选址与供应问题第二问的目标函数
%   注意输入x的符号约定,x(1~12)对应两排运送量Xij,x(13~14)是料场1位置,x(15~16)是料场2位置

a=[1.25 8.75 0.5 5.75 3 7.25];b=[1.25 0.75 4.75 5 6.5 7.75];

% 料场1的运输代价总和
f1 = 0;
for i = 1:6
    s(i) = sqrt((x(13)-a(i))^2+(x(14)-b(i))^2); % 料场1到各工地距离
    f1 = s(i)*x(i)+f1;
end

% 料场2的运输代价总和
f2 = 0;
for i = 7:12
    s(i) = sqrt((x(15)-a(i-6))^2+(x(16)-b(i-6))^2); % 料场2到各工地距离
    f2 = s(i)*x(i)+f2;
end

f = f1+f2;
end

