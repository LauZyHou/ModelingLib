clear;clc;

% 两总体均值的假设检验使用t-检验
x = [23.8,27.6,31.6,32.4,33.7,34.9,43.2,52.8,63.8,73.4];
y = [41.4,51.8,61.7,67.9,68.7,77.5,95.9,137.4,155.0,175.0];
alpha = 0.05; % 显著性水平
tail = 1; % 0:检验假设"x的均值等于y的均值",1:大于,-1:小于
[h,sig,ci] = ttest2(x,y,alpha,tail);

h % 1:可以拒绝假设,0:不能拒绝假设
sig % 假设成立的概率
ci % x与y均值差的1-alpha置信区间